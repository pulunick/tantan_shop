-- =============================================================================
-- 회원 탈퇴(익명화) 지원 컬럼
--
-- 주문 이력이 있는 회원은 orders.user_id 의 ON DELETE RESTRICT(전자상거래법상
-- 주문·결제 기록 보존 의무) 때문에 계정을 즉시 삭제할 수 없다. 이런 경우
-- 계정을 지우는 대신 개인정보를 비식별화하고 로그인을 차단하는 '익명화 탈퇴'로
-- 처리하며, 그 표시를 위해 withdrawn_at 컬럼을 둔다.
--   * NULL          → 정상 회원
--   * timestamptz   → 익명화 탈퇴 처리 시각
-- 주문 row(수령인 정보 포함)는 보존 의무로 삭제/변경하지 않는다.
-- =============================================================================

alter table public.profiles
	add column withdrawn_at timestamptz;

comment on column public.profiles.withdrawn_at is
	'회원 탈퇴(익명화) 시각. NULL 이면 정상 회원. 주문 이력 보유로 계정 완전삭제 대신 익명화한 경우 설정된다.';
